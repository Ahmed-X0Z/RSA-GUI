    private static BigInteger generateRandomPrime(int length, int lengthRange) {
    BigInteger prime;
        int randomLength;

        do {
            long nanoTime = System.nanoTime();
            long currentTime = System.currentTimeMillis();

            RuntimeMXBean runtimeMxBean = ManagementFactory.getRuntimeMXBean();
            long uptime = runtimeMxBean.getUptime();

            int timeBasedRandomness = (int) (nanoTime ^ currentTime ^ uptime);

            randomLength = length + secureRandom.nextInt(lengthRange) + (timeBasedRandomness % lengthRange);
            
            prime = new BigInteger(randomLength, secureRandom);

            if (!prime.testBit(0)) {
                prime = prime.add(BigInteger.ONE);
            }

            if (!prime.testBit(randomLength - 1)) {
                prime = prime.setBit(randomLength - 1);
            }

        } while (!prime.isProbablePrime(100));

        return prime;
    }
    
