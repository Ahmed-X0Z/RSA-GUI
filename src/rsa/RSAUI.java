    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    BigInteger p;
    BigInteger q;

    if (jRadioButton1.isSelected()) {
        q = generateRandomPrime(1024, 16);
    } else {
        q = new BigInteger(jTextArea2.getText().trim());
    }

    if (jRadioButton2.isSelected()) {
        p = generateRandomPrime(1024, 16);
    } else {
        p = new BigInteger(jTextArea1.getText().trim());
    }

    if (!p.isProbablePrime(100) || !q.isProbablePrime(100)) {
        jTextArea3.setText("Provided numbers are not prime. Please enter valid primes or select random generation.");
        return;
    }

    BigInteger n = p.multiply(q);
    BigInteger phiOf_n = (p.subtract(BigInteger.ONE)).multiply(q.subtract(BigInteger.ONE));

    BigInteger e;
    do {
        e = BigInteger.probablePrime(16, secureRandom);
    } while (!e.gcd(phiOf_n).equals(BigInteger.ONE));

    BigInteger d = e.modInverse(phiOf_n);

    jTextArea3.setText("Generated Keys:\n");
    jTextArea3.append("n: " + n + "\n");
    jTextArea3.append("e: " + e + "\n");
    jTextArea3.append("d: " + d + "\n");
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String plainText = jTextArea4.getText().trim();
    String[] keys = jTextArea3.getText().split("\n");
    BigInteger n = new BigInteger(keys[1].split(": ")[1].trim());
    BigInteger e = new BigInteger(keys[2].split(": ")[1].trim()); 

    List<BigInteger> cipherTexts = new ArrayList<>();

    for (char c : plainText.toCharArray()) {
        byte[] charBytes = {(byte) c};
        BigInteger charBigInt = new BigInteger(1, charBytes);

        if (charBigInt.compareTo(n) >= 0) {
            jTextArea5.setText("Error: One or more characters exceed the modulus 'n'.");
            return;
        }

        BigInteger cipherText = charBigInt.modPow(e, n);
        cipherTexts.add(cipherText);
    }

    StringBuilder concatenatedCipherTexts = new StringBuilder("Ciphertexts:\n");
    for (BigInteger cipherText : cipherTexts) {
        concatenatedCipherTexts.append(cipherText.toString()).append("\n");
    }

    jTextArea5.setText(concatenatedCipherTexts.toString()); 
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String cipherTextStr = jTextArea5.getText().trim();
            if (cipherTextStr.isEmpty() || !cipherTextStr.startsWith("Ciphertexts:")) {
                jTextArea6.setText("Error: Invalid ciphertext. Please check the encrypted text.");
                return;
            }

            String[] cipherTextChunks = cipherTextStr.split("Ciphertexts:\n")[1].trim().split("\n");

            String[] keys = jTextArea3.getText().split("\n");
            BigInteger n = new BigInteger(keys[1].split(": ")[1].trim());
            BigInteger d = new BigInteger(keys[3].split(": ")[1].trim());

            StringBuilder decryptedText = new StringBuilder();
            for (String cipherTextChunk : cipherTextChunks) {
                BigInteger cipherText = new BigInteger(cipherTextChunk.trim());

                BigInteger decryptedChunk = cipherText.modPow(d, n);
                decryptedText.append(new String(decryptedChunk.toByteArray()));
            }

            jTextArea6.setText("Decrypted Text:\n" + decryptedText.toString());

        } catch (Exception ex) {
            jTextArea6.setText("Error during decryption. Please check the ciphertext and keys.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    
